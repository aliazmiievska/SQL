        -- imp
        ,DATEADD(YEAR, -2000, докЗаказПокупателя._Date_Time) AS Дата_докЗаказПокупателя
        ,докЗаказПокупателя._Number AS Номер_докЗаказПокупателя
        -- FK
        -- ,докЗаказПокупателя._Fld24458RRef AS FK_спрСтатусыЗаказовПокупателя_докЗаказПокупателя
        -- ,докЗаказПокупателя._Fld5546RRef AS FK_спрКонтрагенты_докЗаказПокупателя
        -- ,докЗаказПокупателя._Fld5536RRef AS FK_ВалютаДокумента_докЗаказПокупателя
        -- other
        -- ,DATEADD(YEAR, -2000, CAST(докЗаказПокупателя._Fld5540 AS datetime2)) AS ДатаОтгрузки_докЗаказПокупателя
            -- datetime2 може мати значення типу 0001-01-01, а в datetime мінімальне - 1753-01-01
        -- system
        -- ,докЗаказПокупателя._Posted AS Проведен_докЗаказПокупателя
        -- ,докЗаказПокупателя._Marked AS ПометкаУдаления_докЗаказПокупателя
    FROM 
        _Document316 AS докЗаказПокупателя

    WHERE 
        докЗаказПокупателя._Posted = 1 
        AND 
        докЗаказПокупателя._Marked = 0
        AND
        докЗаказПокупателя._Date_Time >= '4025-01-01' -- надто величезні масштаби даних, щоб виводити всі роки (це вже 65к)
)


