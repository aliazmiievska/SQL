,Join__спр_ВидыПодразделений__регсведПростои AS (
    SELECT
        *
    FROM
        регсведПростои
        LEFT JOIN
            спр_ВидыПодразделений
            ON
                регсведПростои.FK__Подразделение__спрВидыПодразделений_регсведР
                    = спр_ВидыПодразделений.ID_ВидыПодразделений__регсведПростои
)

,Props__ТривалістьРемонтів__регсведПростои AS (
    SELECT
        *
        ,CASE 
            WHEN Join__спр_ВидыПодразделений__регсведПростои.own__НачалоРемонта_регсведПростои IS NULL 
            OR Join__спр_ВидыПодразделений__регсведПростои.own__КонецРемонта_регсведПростои IS NULL 
            OR Join__спр_ВидыПодразделений__регсведПростои.own__КонецРемонта_регсведПростои <= Join__спр_ВидыПодразделений__регсведПростои.own__НачалоРемонта_регсведПростои
            OR 
                DATEDIFF(
                    MINUTE, Join__спр_ВидыПодразделений__регсведПростои.own__НачалоРемонта_регсведПростои, Join__спр_ВидыПодразделений__регсведПростои.own__КонецРемонта_регсведПростои
                ) > 720
                THEN 
                    0
            ELSE 
                DATEDIFF(
                    MINUTE, Join__спр_ВидыПодразделений__регсведПростои.own__НачалоРемонта_регсведПростои, Join__спр_ВидыПодразделений__регсведПростои.own__КонецРемонта_регсведПростои
                )
        END AS DurationMinutes_Ремонты
    FROM 
        Join__спр_ВидыПодразделений__регсведПростои
)

,Props__РемонтЗміна__регсведПростои AS (
    SELECT
        *
        ,CASE 
            WHEN 
                DATEPART(
                    HOUR, own__НачалоРемонта_регсведПростои
                ) >= 6 
            AND 
                DATEPART(
                    HOUR, own__НачалоРемонта_регсведПростои
                ) < 18
                THEN 
                    CONVERT(
                        nvarchar(10), 
                            own__НачалоРемонта_регсведПростои
                    ) + N', Денна, ' + Назва_ВидыПодразделений__регсведПростои
            ELSE 
                CASE 
                    WHEN 
                        DATEPART(
                                HOUR, own__НачалоРемонта_регсведПростои
                        ) >= 18
                        THEN
                        CONVERT(
                            nvarchar(10), 
                                own__НачалоРемонта_регсведПростои
                        ) + N', Нічна, ' + Назва_ВидыПодразделений__регсведПростои
                    ELSE
                        CONVERT(
                            nvarchar(10), 
                                calc__НачалоРемонтаМінусДень_регсведПростои
                        ) + N', Нічна, ' + Назва_ВидыПодразделений__регсведПростои
                END
        END AS Зміна_Ремонты
    FROM 
        Props__ТривалістьРемонтів__регсведПростои
)

,Agr__СумаРемонтівПоЗміні__регсведПростои AS (
    SELECT
        Зміна_Ремонты
        ,SUM(DurationMinutes_Ремонты) AS TotalDurationMinutes_Ремонты
    FROM 
        Props__РемонтЗміна__регсведПростои
    GROUP BY
        Зміна_Ремонты
)

