,Join__спр_ВидыПодразделений_Рем__регсведРемонты AS (
    SELECT
        *
    FROM
        регсведРемонты
        LEFT JOIN
            спр_ВидыПодразделений_Рем
            ON
                регсведРемонты.FK__Подразделение__спрВидыПодразделений_регсведР
                    = спр_ВидыПодразделений_Рем.ID_ВидыПодразделений__регсведРемонты
)

,Props__ТривалістьРемонтів__регсведРемонты AS (
    SELECT
        *
        ,CASE 
            WHEN Join__спр_ВидыПодразделений_Рем__регсведРемонты.own__НачалоРемонта_регсведРемонты IS NULL 
            OR Join__спр_ВидыПодразделений_Рем__регсведРемонты.own__КонецРемонта_регсведРемонты IS NULL 
            OR Join__спр_ВидыПодразделений_Рем__регсведРемонты.own__КонецРемонта_регсведРемонты <= Join__спр_ВидыПодразделений_Рем__регсведРемонты.own__НачалоРемонта_регсведРемонты
            OR 
                DATEDIFF(
                    MINUTE, Join__спр_ВидыПодразделений_Рем__регсведРемонты.own__НачалоРемонта_регсведРемонты, Join__спр_ВидыПодразделений_Рем__регсведРемонты.own__КонецРемонта_регсведРемонты
                ) > 720
                THEN 
                    0
            ELSE 
                DATEDIFF(
                    MINUTE, Join__спр_ВидыПодразделений_Рем__регсведРемонты.own__НачалоРемонта_регсведРемонты, Join__спр_ВидыПодразделений_Рем__регсведРемонты.own__КонецРемонта_регсведРемонты
                )
        END AS DurationMinutes_Ремонты
    FROM 
        Join__спр_ВидыПодразделений_Рем__регсведРемонты
)

,Props__РемонтЗміна__регсведРемонты AS (
    SELECT
        *
        ,CASE 
            WHEN 
                DATEPART(
                    HOUR, own__НачалоРемонта_регсведРемонты
                ) >= 6 
            AND 
                DATEPART(
                    HOUR, own__НачалоРемонта_регсведРемонты
                ) < 18
                THEN 
                    CONVERT(
                        nvarchar(10), 
                            own__НачалоРемонта_регсведРемонты
                    ) + N', Денна, ' + Назва_ВидыПодразделений__регсведРемонты
            ELSE 
                CASE 
                    WHEN 
                        DATEPART(
                                HOUR, own__НачалоРемонта_регсведРемонты
                        ) >= 18
                        THEN
                        CONVERT(
                            nvarchar(10), 
                                own__НачалоРемонта_регсведРемонты
                        ) + N', Нічна, ' + Назва_ВидыПодразделений__регсведРемонты
                    ELSE
                        CONVERT(
                            nvarchar(10), 
                                calc__НачалоРемонтаМінусДень_регсведРемонты
                        ) + N', Нічна, ' + Назва_ВидыПодразделений__регсведРемонты
                END
        END AS Зміна_Ремонты
    FROM 
        Props__ТривалістьРемонтів__регсведРемонты
)

,Agr__СумаРемонтівПоЗміні__регсведРемонты AS (
    SELECT
        Зміна_Ремонты
        ,SUM(DurationMinutes_Ремонты) AS TotalDurationMinutes_Ремонты
    FROM 
        Props__РемонтЗміна__регсведРемонты
    GROUP BY
        Зміна_Ремонты
)

